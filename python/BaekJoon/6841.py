while 1 : 
    s = input()

    if s == "TTYL" : 
        print("talk to you later")
        break
    else : 
        if s == "CU" : print("see you")
        elif s == ":-)" : print("I’m happy")
        elif s == ":-(" : print("I’m unhappy")
        elif s == ";-)" : print("wink")
        elif s == ":-P" : print("stick out my tongue")
        elif s == "(~.~)" : print("sleepy")
        elif s == "TA" : print("totally awesome")
        elif s == "CCC" : print("Canadian Computing Competition")
        elif s == "CUZ" : print("because")
        elif s == "TY" : print("thank-you")
        elif s == "YW" : print("you’re welcome")
        else : print(s)